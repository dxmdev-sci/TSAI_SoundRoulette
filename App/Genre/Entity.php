<?php
require_once(DB_DIR."/Entity.php");
class GenreEntity extends Entity {
    private $id;
    private $name;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

}